package com.example.retrofithe;

public class Post {
    private int id;
    private String Name;
    private String email;
    private String gender;
    private String status;

    public int getId() {
        return id;
    }

    public String getName() {
        return Name;
    }

    public String getEmail() {
        return email;
    }

    public String getGender() {
        return gender;
    }

    public String getStatus() {
        return status;
    }
}
